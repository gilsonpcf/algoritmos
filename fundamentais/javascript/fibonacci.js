// Algoritmo Fundamental Nº 6
// Descrição : Geração da sequência de Fibonacci.
// Problema  : Gerar e imprimir os N primeiros termos da sequência de Fibonacci,
//             onde N > 1. Os primeiros termos são: 0, 1, 1, 2, 3, 5, 8, 13, ...
//             Cada termo, além dos dois primeiros, é derivado da soma de seus
//             dois antecessores mais próximos.
// Aplicações: Botânica, teoria da rede elétrica, classificação e pesquisa de dados.

var n = prompt("Informe o número de termos da sequência:");

var termo1 = 0;
var termo2 = 1;
var termo3;

document.write(termo1 + ", " + termo2);

for (i = 3; i <= n; i++) {
    termo3 = termo1 + termo2;
    document.write(", " + termo3);
    termo1 = termo2;
    termo2 = termo3;
}
